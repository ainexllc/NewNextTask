import * as React from "react"
import { cn } from "@/lib/utils"

const Card = React.forwardRef<
  HTMLDivElement,
  React.HTMLAttributes<HTMLDivElement> & {
    glass?: boolean
  }
>(({ className, glass = false, ...props }, ref) => (
  <div
    ref={ref}
    className={cn(
      "rounded-xl border border-border/20 bg-white dark:bg-card text-card-foreground shadow-[0_1px_3px_0_rgb(0,0,0,0.1)] hover:shadow-[0_4px_12px_0_rgb(0,0,0,0.15)] adaptive-transition",
      glass && "glass-effect border-glass",
      className
    )}
    {...props}
  />
))
Card.displayName = "Card"

